    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'name' => 'required',
            'email' => 'required',
            'epf' => 'required',
            'department' => 'required',
            'designation' => 'required',

        ]);

        $newEmp = new Employee;

        $newEmp->name=$request->input('name');
        $newEmp->email=$request->input('email');
        $newEmp->epf=$request->input('epf');
        $newEmp->department=$request->input('department');
        $newEmp->designation=$request->input('designation');
        $newEmp->sal_grp=$request->input('sal_grp');
        $newEmp->save();

        return back();
    }

